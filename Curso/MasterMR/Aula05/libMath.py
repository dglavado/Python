import math

print(round(5.2))
print(round(5.7))

print(math.ceil(5.2)) #arredonda para cima
print(math.floor(5.7)) #arredonta para baixo - parte inteira (int)

print(math.sqrt(9)) #raiz quadrada

print(math.factorial(5)) #fatorial
print(math.fabs(-7.8)) #valor absoluto (remove sinal)


print('Fim')